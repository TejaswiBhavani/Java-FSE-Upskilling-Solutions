package basics;

public class TypeCastingExample {
    public static void main(String[] args) {
        double d = 9.99;
        int i = (int) d;
        System.out.println("Double to int: " + i);
        int num = 10;
        double numD = num;
        System.out.println("Int to double: " + numD);
    }
}
